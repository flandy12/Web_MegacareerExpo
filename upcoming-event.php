                                    <p>

                                        <strong>1. Apakah Mega Career Expo (MCE) itu ?</strong> <br />

                                        Mega Career Expo adalah offline jobfair tempat kamu bisa mencari kerja dan
                                        bertemu langsung dengan pihak HRD. Semenjak masa pandemi yang semakin
                                        terkontrol, Mega Career Expo akan kembali hadir di Tanah Air untuk memberikan
                                        Bursa Kerja yang Terbaik, Terbesar dan Terpercaya bagi perusahaan maupun pencari
                                        kerja.

                                        <br /><br />

                                        <strong>2. Mengapa harus melamar di MCE ? </strong> <br />

                                        Pertama, karena diadakan secara offline, kamu bisa langsung bertatap muka dengan
                                        HRD dan menyerahkan CV kamu secara langsung ke masing-masing perusahaan. Selain
                                        itu, tak hanya bisa melamar kerja secara langsung, kamu juga bisa mengikuti sesi
                                        talkshow seputar karir dan dunia kerja “OKE Talks” yang dapat menambah wawasan
                                        kamu.

                                        <br /><br />

                                        <strong>3. Kapan MCE akan dilaksanakan? </strong> <br />

                                        Jadwal offline MCE yang dapat kamu ikuti yaitu : <br />
                                        
                                        (1) Career Expo Medan, 16-17 Mei 2023 di Gedung Pancasila(Pendopo USU), 10.00 – 16.00 wib <br />
                                        (2) Mega Career Expo Jakarta, 14-15 Juli 2023 di Ged. Smesco Exhibition Hall, 10.00 – 17.00 wib <br />
                                        (3) Mega Career Expo Jakarta, 6-7 Oktober 2023 di GBK Senayan, 10.00 – 17.00 wib <br /><br />

                                        <strong>4. Apakah yang dimaksud dengan Segmen “OKE Talks” di MCE Series ?
                                        </strong><br />

                                        OKE Talks adalah salah satu segmen talkshow yang akan ada di MCE Series. Segmen
                                        ini akan menghadirkan pembicara berkredibilitas dan professional di bidangnya
                                        baik dibidang HR, Manajemen SDM, Coaching, Trainer dan lainnya. OKE Talks
                                        merupakan singkatan dari Obrolin Karir dan lokEr, dimana tema-tema yang akan
                                        dibahas berkaitan dengan dunia kerja dan karir serta peluang-peluang kerja yang
                                        ada di MCE Series. Pencaker dapat mengikuti segmen ini secara free dan terbuka
                                        untuk semua kalangan.

                                        <br /><br />

                                        <strong>5. Bagaimana cara melamar di MCE? </strong><br />

                                        Berikut ini langkah kamu melamar kerja di Mega Career Expo Series : <br />

                                        1. Akses <a href="https://www.sambilanku.id/mce/register"
                                            target="_blank">www.sambilanku.id/mce/register</a> untuk melakukan
                                        registrasi. <br />

                                        2. Lengkapi data diri saat registrasi dan pastikan setiap kolom terisi dengan
                                        data yang benar dan <i>up to date</i> serta pilih event jobfair
                                        yang<br>&nbsp;&nbsp;&nbsp; hendak kamu kunjungi. <br />

                                        3. Setelah selesai mengisi silahkan klik “Submit” <br />

                                        4. Peserta dapat cek inbox/spam di email yang telah didaftarkan untuk
                                        mendapatkan QR Code yang harus ditunjukkan ke panitia MCE<br>&nbsp;&nbsp;&nbsp;
                                        saat kamu datang ke lokasi acara untuk registrasi ulang. <br />

                                        5. Setelahnya, kamu tinggal datang ke lokasi acara sesuai tanggal dan jam yang
                                        telah kamu pilih dengan membawa CV hardcopy sebanyak-<br>&nbsp;&nbsp;&nbsp;
                                        banyaknya. <br />

                                        6. Pastikan pula tetap menerapkan protokol kesehatan selama berada di area
                                        event.

                                        <br /><br />

                                        <strong>6. Siapa saja yang dapat mengikuti MCE ini ? </strong><br />

                                        MCE ini terbuka untuk umum bagi setiap peserta yang sedang membutuhkan informasi
                                        lowongan pekerjaan baik Pria maupun Wanita. Adapun minimal usia peserta yaitu 18
                                        tahun keatas. Sedangkan dari sisi pendidikan akhir minimal SMA/K hingga lulusan
                                        Sarjana (S1/S2) semua jurusan. Lowongan di event ini juga terbuka untuk semua
                                        kalangan mulai dari <i>Fresh Graduate</i> maupun berpengalaman.

                                        <br /><br />

                                        <strong>7. Apakah MCE ini berbayar atau tidak? </strong><br />

                                        Pencaker tidak kenakan biaya untuk registrasi reguler yang dilakukan. Dan bagi
                                        pencaker yang telah terdaftar sebagai Member Premium sebelumnya dapat menerima
                                        fasilitas “FastLane” pada saat event berlangsung (jalur bebas antri) dengan
                                        catatan masa Member Premium masih berlaku.

                                        <br /><br />

                                        <strong>8. Dimanakah kamu dapat mengupdate informasi seputar MCE?
                                        </strong><br />

                                        Kamu dapat mengupdate semua informasi mengenai MCE di <a
                                            href="https://www.instagram/megacareerexpo"
                                            target="_blank">IG@megacareerexpo</a> dan website <a
                                            href="https://www.megacareerexpo.com"
                                            target="_blank">www.megacareerexpo.com</a>. Di sini kamu bisa cek secara
                                        berkala perusahaan peserta serta jadwal segmen “OKE TALKS : Obrolin Karir dan
                                        lokEr” untuk masing-masing event. Jadi pastikan kamu follow IG @megacareerexpo
                                        yah! Kamu juga bisa menghubungi hotline kami di <a
                                            href="https://wa.me/6287887931290" target="_blank">wa.me/6287887931290</a>.

